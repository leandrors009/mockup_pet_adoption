class Pet {
  final String imagePath;
  final String name;
  final String breed;
  final String gender;
  final DateTime age;
  final double distance;
  final bool isLiked;

  Pet({
    required this.imagePath,
    required this.name,
    required this.breed,
    required this.gender,
    required this.age,
    required this.distance,
    required this.isLiked,
  });
}
